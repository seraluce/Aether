function pickIcon(name: string, slug: string): string {
  const str = name + slug;
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = (hash << 5) - hash + str.charCodeAt(i);
    hash |= 0;
  }
  return svgIcons[Math.abs(hash) % svgIcons.length];
}

export function mapCategory(cat: WPCategory): {
  name: string;
  slug: string;
  count: number;
  icon: string;
} {
  return {
    name: cat.name,
    slug: cat.slug,
    count: cat.count,
    icon: pickIcon(cat.name, cat.slug),
  };
}

